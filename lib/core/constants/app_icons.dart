  // ===== دوال مساعدة =====

  // الحصول على أيقونة الحالة
  static IconData getStatusIcon(String status) {
    switch (status.toLowerCase()) {
      case 'success':
      case 'paid':
      case 'completed':
        return success;
      case 'warning':
      case 'pending':
        return warning;
      case 'error':
      case 'failed':
      case 'cancelled':
        return error;
      case 'info':
      case 'active':
        return info;
      default:
        return info;
    }
  }

  // الحصول على أيقونة نوع الدفع
  static IconData getPaymentMethodIcon(String method) {
    switch (method.toLowerCase()) {
      case 'cash':
      case 'نقدي':
        return cash;
      case 'card':
      case 'بطاقة':
        return credit;
      case 'bank':
      case 'بنكي':
        return bank;
      default:
        return money;
    }
  }

  // إنشاء أيقونة مع لون مخصص
  static Widget createColoredIcon(
    IconData icon, {
    Color? color,
    double? size,
    String? semanticLabel,
  }) {
    return Icon(
      icon,
      color: color,
      size: size,
      semanticLabel: semanticLabel,
    );
  }

  // إنشاء أيقونة مع خلفية دائرية
  static Widget createCircularIcon(
    IconData icon, {
    Color? backgroundColor,
    Color? iconColor,
    double? radius,
    double? iconSize,
  }) {
    return CircleAvatar(
      radius: radius ?? 20,
      backgroundColor: backgroundColor ?? AppColors.primary,
      child: Icon(
        icon,
        color: iconColor ?? Colors.white,
        size: iconSize ?? 20,
      ),
    );
  }
